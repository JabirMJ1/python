from math import *
n = float(input("Enter the number: "))

print(f"Floored value: {floor(n)}")
print(f"Ceiled value: {ceil(n)}")
print(f"Absolute value: {abs(n)}")
print(f"Log value: {log(n)}")
print(f"Log10 value: {log10(n)}")
print(f"Modf value: {modf(n)}")
print(f"Exponential value: {exp(n)}")
print(f"FAbsolute value: {fabs(n)}")
print(f"Square root: {sqrt(n)}")

m = float(input("Enter another number: "))
print(f"Power x**y value: {pow(n, m)}")
print(f"min value: {min(n, m)}")
print(f"min value: {max(n, m)}")

